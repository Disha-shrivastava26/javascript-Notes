// type coercion

// +
console.log(2 + 4 + 5 + "9"); //119
// -
console.log("99" - "11"); //88

// *
console.log("99" * "11"); //1089
console.log(99 * "11"); //convert string to number

//  /
console.log("99" / "11"); // convert the string to number
